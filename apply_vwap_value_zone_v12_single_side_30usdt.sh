import json
from index_sniper import vwap_video_live_v1 as v

client = v.make_client()
positions = [
    row for row in v.fetch_positions(client)
    if abs(v.safe_float(row.get("total"))) > 0
]
orders = v.fetch_open_orders(client)
strategies = v.fetch_strategy_orders(client)
report = {
    "positions": len(positions),
    "open_orders": len(orders),
    "strategy_orders": len(strategies),
}
print(json.dumps(report, ensure_ascii=False, indent=2))
if positions or orders or strategies:
    raise SystemExit(
        "❌ 설치 전 계정이 완전히 비어 있어야 합니다. "
        "앱에서 포지션을 종료하고 모든 일반/TP-SL 주문을 취소하세요."
    )
print("✅ flat account confirmed")
PYFLAT
fi

mkdir -p "$BACKUP" "$ROOT/logs" "$ROOT/data/vwap_video_live_v1" "$ROOT/research"

